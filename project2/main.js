rightButton.addEventListener('click', () => scrollLinks(1)); 

function scrollLinks(direction) {
    const newScrollLeft = linksContainer.scrollLeft + direction * totalLinkWidth; 

    const maxScrollLeft = linksContainer.scrollWidth - linksContainer.clientWidth;
    const clampedScrollLeft = Math.min(Math.max(newScrollLeft, 0), maxScrollLeft);

    linksContainer.scrollTo({
        left: clampedScrollLeft,
        behavior: 'smooth'
    });
}

