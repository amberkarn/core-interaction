function renderContent() {
    const tasksToRender = []; 

    toolbox.innerHTML = '';

    const shuffledTasks = shuffleArray(tasks);
    shuffledTasks.forEach(task => {
        if (showCategory === 'all' || task.category.includes(showCategory)) {
            tasksToRender.push(task);
        }
    });

    tasksToRender.forEach((task, index) => {
        const taskElement = document.createElement('div');
        taskElement.className = 'task fade-in'; 

        const img = document.createElement('img');
        img.src = 'img/' + task.img;
        img.loading = 'eager'
        img.alt = task.title;
        let filterTimeout; 

        taskElement.addEventListener('mouseenter', () => {
            if (!taskElement.classList.contains('selected')) {
                img.style.filter = 'grayscale(70%) brightness(8%) sepia(100%) hue-rotate(0deg) saturate(7000%)';            }
        });
        
        taskElement.addEventListener('mouseleave', () => {
            if (!taskElement.classList.contains('selected')) {
                img.style.filter = 'none'; // Reset filter
            }
        });
               
        taskElement.addEventListener('click', () => {
            document.querySelectorAll('.task').forEach(element => {
                element.classList.remove('selected');
                element.querySelector('img').style.filter = 'none'; 
            });
        
            taskElement.classList.add('selected');
            img.style.filter = 'grayscale(70%) brightness(8%) sepia(100%) hue-rotate(0deg) saturate(7000%)';
        
            clearTimeout(filterTimeout);
        });
        



        taskElement.appendChild(img);
        toolbox.appendChild(taskElement);

        taskElement.addEventListener('click', () => {
            upLeftMenu(task); 
        });

        taskElement.style.opacity = '0';
        taskElement.style.transition = 'opacity 0.5s ease';

        setTimeout(() => {
            taskElement.style.opacity = '1';
        }, index * 100); 
    });
}   


function upLeftMenu(task) {
    leftMenu.innerHTML = '';

    const propertiesToDisplay = ["category", "who", "what", "where", "id"];

    const imageContainer = document.createElement('div');
    imageContainer.style.position = 'relative'; 
    imageContainer.style.height = '40%'; 

    const enlargedImg = document.createElement('img');
    enlargedImg.src = 'img/' + task.img;
    enlargedImg.alt = task.title;
    enlargedImg.classList.add('enlarged-image'); 


    imageContainer.appendChild(enlargedImg);

    const textContainer = document.createElement('div');
    textContainer.style.position = 'absolute'; 
    textContainer.style.top = '0.5rem'; 
    textContainer.style.left = '2rem'; 
    textContainer.style.lineHeight = '1rem'; 
    textContainer.style.width = '100%'; 
    textContainer.style.height = '10%'; 

    propertiesToDisplay.forEach(prop => {
        if (task.hasOwnProperty(prop)) {
            const propertyElement = document.createElement('div');
            propertyElement.textContent = `${prop}: ${task[prop]}`;
            textContainer.appendChild(propertyElement);
            propertyElement.classList.add('property-text');
        }
    });

    leftMenu.appendChild(imageContainer);
    leftMenu.appendChild(textContainer);

    addHomeButton();

    requestAnimationFrame(() => {
        let opacity = 0;
        const fadeIn = () => {
            opacity += 0.04; 
            enlargedImg.style.opacity = opacity;
            if (opacity < 1) {
                requestAnimationFrame(fadeIn); 
            }
        };
        fadeIn();
    });
    addHomeButton();
}





function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

