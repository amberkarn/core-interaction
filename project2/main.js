const toolbox = document.querySelector('#toolbox');
const categoryLinks = document.getElementById('category-links');
const homepageButton = document.querySelector('.top-left-button');
const leftMenu = document.querySelector('.left-menu');
const bigTitle = document.querySelector('.big-title');
const bodyText = document.querySelector('.body-text');
const categoryPlaceholder = document.getElementById('category-placeholder');

let showCategory = 'all';

renderContent();
updateControls();

function renderContent() {
    toolbox.innerHTML = '';
    bigTitle.style.display = 'none'; // Hide big title
    
    tasks.forEach(task => {
        if (showCategory === 'all' || task.category === showCategory) {
            const taskElement = document.createElement('div');
            taskElement.className = 'task';
            taskElement.setAttribute('data-id', task.id); // Set id attribute

            const img = document.createElement('img');
            img.src = 'img/' + task.img;
            img.alt = task.title;
            img.addEventListener('click', function() {
                duplicateImageInLeftMenu(task);
            });

            taskElement.appendChild(img);
            toolbox.appendChild(taskElement);
        }
    });

    // Update left menu content
    leftMenu.innerHTML = `
        <div class="big-title" style="margin-bottom: 20px;">
            Collections: A Fashion Archive
        </div>
        <div class="body-text" style="text-align: center;">
        A historical and modern archive of stylists, models and fashion photographers. Curated by Amber Pjongluck
        </div>
    `;

    // Update left menu category placeholder text
    if (showCategory !== 'all') {
        categoryPlaceholder.textContent = `Placeholder text for ${showCategory}`;
    } else {
        categoryPlaceholder.textContent = `A historical and modern archive of stylists, models and fashion photographers. Curated by Amber Pjongluck`;
    }
}

function duplicateImageInLeftMenu(task) {
    leftMenu.innerHTML = `
        <div class="big-title" style="margin-bottom: 20px;">
            ${task.category}
        </div>
        <img src="img/${task.img}" alt="${task.title}" style="width: 100%; max-width: 300px; height: auto;">
        <div class="body-text" style="text-align: left;">
            <p><strong>Category:</strong> ${task.category}</p>
            <p><strong>editorial:</strong> ${task.editorial}</p>
            <p><strong>Walking:</strong> ${task.walking ? 'Yes' : 'No'}</p>
            <p><strong>Date:</strong> ${task.date}</p>
        </div>
    `;
}

function updateControls() {
    categoryLinks.addEventListener('click', function(event) {
        if (event.target.tagName === 'A') {
            event.preventDefault();
            showCategory = event.target.textContent.trim();
            renderContent();
        }
    });

    homepageButton.addEventListener('click', function(event) {
        event.preventDefault();
        showCategory = 'all';
        renderContent();
    });
}

// Add event listener to each task element (image)
toolbox.addEventListener('click', function(event) {
    if (event.target.classList.contains('task')) {
        const taskId = parseInt(event.target.dataset.id);
        const clickedTask = tasks.find(task => task.id === taskId);
        duplicateImageInLeftMenu(clickedTask);
    }
});

