const photoGrid = document.querySelector('.photo-grid');

photoGrid.addEventListener('wheel', function(event) {
    event.preventDefault();
    const delta = event.deltaY;

    console.log(event.deltaY)

    const scrollSpeed = Math.abs(delta); 
    const smoothScrollAmount = 0;
    function smoothScroll() {
        if (delta < 0) {
            photoGrid.scrollLeft -= scrollSpeed;
        } else {
            photoGrid.scrollLeft += scrollSpeed;
        }

        if (delta < 0 ? photoGrid.scrollLeft > lastScrollPosition : photoGrid.scrollLeft < lastScrollPosition) {
            requestAnimationFrame(smoothScroll);
        }
    }

    lastScrollPosition = delta > 0 ? photoGrid.scrollLeft - smoothScrollAmount : photoGrid.scrollLeft + smoothScrollAmount;

    smoothScroll();
});