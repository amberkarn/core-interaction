;function upControls() {
    categoryLinks.addEventListener('click', function(event) {
        if (event.target.tagName === 'A') {
            event.preventDefault();
            const selectedCategory = event.target.textContent.trim();
            showCategory = (showCategory === selectedCategory) ? 'all' : selectedCategory;

            lastScrollPosition = 0;
            photoGrid.scrollLeft = lastScrollPosition;
            document.querySelectorAll('.links a').forEach(link => {
                link.classList.remove('selected');
            });

            event.target.classList.toggle('selected');

            renderContent();
            displayLeftMenuText(selectedCategory, placeholderTexts);
            addHomeButton();
        }
    });
}

